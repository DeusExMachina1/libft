#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;
	int		k;
	char	d;

	i = 0;
	k = 1;
	if (n != 1)
	{
		d = (n % 10) + 48;
		n = n / 10;
	}
	if (n >= 10)
		ft_putnbr(n);
	write(1, &d, 1);
}

int		main(void)
{
	int num = 11234;
	ft_putnbr(num);
	return (0);
}
