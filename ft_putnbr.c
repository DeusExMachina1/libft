#include "libft.h"

void	ft_putnbr(int n)
{
	char d;

	if (n >= 9)
	{
		d = (n % 10) + 48;
		n = n / 10;
		ft_putnbr(n);
	}
	else
		d = n + 48;
	write(1, &d, 1);
}
