char	*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	while (*src != '\0')
	{
		dst[i] = *src;
		src++;
		i++;
	}
	i++;
	dst[i] = '\0';
	return (&dst[0]);
}
